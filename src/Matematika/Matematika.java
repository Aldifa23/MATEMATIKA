package Matematika;

/**
 *
 * @author Difa
 */
public class Matematika {
     int angka1;
    int angka2;

 
    public Matematika(int angka1, int angka2) {
        this.angka1 = 3;
        this.angka2 = 5;
    }

    public int pengurangan() {
        return angka1 - angka2;
    }

    public int pertambahan() {
        return angka1 + angka2;
    }

    public int modulus() {
        return angka1 % angka2;
    }
}
